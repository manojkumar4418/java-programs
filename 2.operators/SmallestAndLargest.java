class SmallestAndLargest {
 
	public static void main(String[] args) {
 
		float x = 4.25f;
		int y =5;
        
        //find the largest number of 2 numbers, you have to use Math.max().
 
		System.out.println("The largest number of the two numbers is " + Math.max(x,y));

        // find the smallest number of 2 numbers, you have to use Math.min().
		
        System.out.println("The smallest number of the two numbers is " + Math.min(x,y));
	}
}